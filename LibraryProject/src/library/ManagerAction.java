    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        studentInfoList = StudentModify.findAll();
        bookInfoList = BookModify.findAll();
        String ma_sach = jTextField2.getText();
        String mssv = jTextField3.getText();
        int check_1 =0,check_2=0;
        for(Student studentInfo:studentInfoList){
            if(mssv.equals(studentInfo.getMssv())){
                check_1 =1;
                break;
            }
            else{check_1=0;}
        }
        if(check_1==1)
        {
            for(Book bookInfo:bookInfoList){
                if(ma_sach.equals(bookInfo.getMa_sach())){check_2 = 1;break;}
                else {check_2 =0;}
            }
            if(check_2==1)
            {
                HistoryModify.insertHistory(ma_sach,mssv);
                historyView();
                showAllHistory();
            }
            else {JOptionPane.showMessageDialog(null,"Book_ID is invalid.");}
        }else{JOptionPane.showMessageDialog(null,"Student_ID is invalid.");}


    }//GEN-LAST:event_jButton3ActionPerformed


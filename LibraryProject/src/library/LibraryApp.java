    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int check = 0;
        studentInfoList = StudentModify.findAll();
        nhanvienInfoList = ManagerModify.findAll();
        String logname = jTextField1.getText();
        String password = jPasswordField1.getText();
        for (Student info : studentInfoList) {
            if (logname.equals(info.getMssv()) && password.equals(info.getMat_khau())) {
                check = 1;
                studentlogin = info;
//                        JOptionPane.showMessageDialog(null,"dang nhap thanh cong");
                break;
            } else {
                check = 0;
            }
        }
        if (check == 1) {
//                    JOptionPane.showMessageDialog(null,"dang nhap thanh cong");
            this.setVisible(false);
            StudentAction studentOpen = new StudentAction(studentlogin);
            studentOpen.setLocationRelativeTo(null);
            studentOpen.setVisible(true);

        } else {
//                    JOptionPane.showMessageDialog(null, "dang nhap that bai");
            for (Manager info : nhanvienInfoList) {
//                        System.out.println(info.getMa_nhan_vien()+info.getMat_khau());

                if (logname.equals(info.getMa_nhan_vien()) && password.equals(info.getMat_khau())) {
                    check = 2;
                    nhanvienlogin = info;
                    break;
                }

            }
            System.out.println(check);
            if (check == 2) {
                this.setVisible(false);
                ManagerAction nhanVien = new ManagerAction(nhanvienlogin);
                nhanVien.setLocationRelativeTo(null);
                nhanVien.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Login failed.");
            }
        }
    }

